const downloadQuiz = (quiz, score, submitted) => {
  let content = "<h1>Quiz Results</h1>";
  if (submitted) {
    const pct = Math.round((score / quiz.length) * 100);
    content += "<p class='meta'>Score: " + score + "/" + quiz.length + " (" + pct + "%)</p>";
  }
  quiz.forEach((q, i) => {
    content += "<h2>Q" + (i + 1) + ". " + q.q + "</h2><ul>";
    q.options.forEach((opt, ai) => {
      const correct = ai === q.answer;
      const style = correct ? "color:#16a34a;font-weight:700;" : "";
      content += "<li style='" + style + "'>" + ["A","B","C","D"][ai] + ". " + opt + (correct ? " ✓" : "") + "</li>";
    });
    content += "</ul>";
    if (q.explanation) {
      content += "<p style='background:#f3f0ff;padding:10px;border-radius:6px;font-size:14px;'>💡 " + q.explanation + "</p>";
    }
  });
  downloadHtml(content, "quiz.html", "VidMind Quiz");
};

