const extractVideoId = (url) => {
  const patterns = [
    /(?:youtube\.com\/watch\?v=|youtu\.be\/|youtube\.com\/embed\/)([^&\n?#]+)/,
    /^([a-zA-Z0-9_-]{11})$/,
  ];
  for (const p of patterns) {
    const m = url.match(p);
    if (m) return m[1];
  }
  return null;
};

const fetchTranscript = async (videoId) => {
  const res = await fetch(`${transcript_api}${videoId}`);
  const data = await res.json();
  if (data.error) throw new Error(data.error);
  return data.transcript;
};

const callClaude = async (prompt) => {
  const res = await fetch(claude_api, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ prompt }),
  });
  const data = await res.json();
  if (data.error) throw new Error(data.error);
  return data.text || "";
};

const parseQuiz = (raw) => {
  if (!raw) return null;

  let clean = raw
    .replace(/```json/gi, "")
    .replace(/```/g, "")
    .trim();

  // Attempt 1: direct parse
  try { return JSON.parse(clean); } catch (e){
    console.error("Quiz parse error:", e);
  }

  // Attempt 2: extract [...] block
  try {
    const match = clean.match(/\[[\s\S]*\]/);
    if (match) return JSON.parse(match[0]);
  } catch (e){
    console.error("Quiz parse error:", e);
  }

  // Attempt 3: slice from first [ to last ]
  try {
    const start = clean.indexOf("[");
    const end = clean.lastIndexOf("]");
    if (start !== -1 && end !== -1 && end > start)
      return JSON.parse(clean.slice(start, end + 1));
  } catch(e) {
    console.error("Quiz parse error:", e);
  }

  // Attempt 4: fix trailing commas then parse
  try {
    const sliced = clean.slice(clean.indexOf("["), clean.lastIndexOf("]") + 1);
    const fixed = sliced.replace(/,\s*([}\]])/g, "$1");
    return JSON.parse(fixed);
  } catch (e){
    console.error("Quiz parse error:", e);
  }

  return null;
};

const downloadTxt = (content, filename) => {
  const plain = content
    .replace(/<h2>(.*?)<\/h2>/g, "\n\n== $1 ==\n")
    .replace(/<h3>(.*?)<\/h3>/g, "\n-- $1 --\n")
    .replace(/<strong>(.*?)<\/strong>/g, "$1")
    .replace(/<li[^>]*>(.*?)<\/li>/g, "  - $1\n")
    .replace(/<ul>(.*?)<\/ul>/gs, "$1")
    .replace(/<br\/?>/g, "\n")
    .replace(/<[^>]+>/g, "")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
  const blob = new Blob([plain], { type: "text/plain" });
  const a = document.createElement("a");
  a.href = URL.createObjectURL(blob);
  a.download = filename;
  a.click();
  URL.revokeObjectURL(a.href);
};

